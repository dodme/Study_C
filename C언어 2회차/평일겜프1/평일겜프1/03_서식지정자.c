#include <stdio.h>

// ; - 세미콜론 // 구문의 끝을 알림
// : - 콜론

//  : 주석 / 메모
/// : 주석

int main()
{
	// 서식 지정자
	/// %d : 정수
	/// %f : 실수 소숫점 6번째자리 까지
	/// %lf : 실수 소숫점 15번째자리 까지
	/// %c : 문자
	/// %s : 문자열


	printf("Character HP : %d \n", 100);

	printf("실수 출력 : %.1f \n", 1.4);

	printf("문자 출력 : %c \n", 'A');

	// "문자열"
	// '문자'

	printf("문자열 출력 : %s \n", " 무야~호");

	return 0;
}

